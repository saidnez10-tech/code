# PROGRAM TO FIND ASCII VALUE OF A CHARACTER
# ----------------------------------------

# Ask the user to enter a character and store it in a variable.
user_char = input("Enter a character: ")

# Step 2:
# Convert the character into its ASCII value using ord(), and store it in a variable
ascii_value = ord(user_char)

# Step 3:
# Print the character entered by the user.
print("Character entered:", user_char)

# Step 4:
# Print the ASCII value of the character.
print("ASCII value:", ascii_value)